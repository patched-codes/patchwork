/**
 * Executes an SQL query on a given SQLite database and returns the results.
 * 
 * @param db A pointer to an open SQLite database connection.
 * @param query The SQL query to be executed on the database.
 * @return A vector of vectors containing the query results, where each inner vector represents a row of results.
 */
std::vector<std::vector<std::string>> sqlite(sqlite3* db, const std::string& query) {
    std::vector<std::vector<std::string>> results;
    sqlite3_stmt* stmt;

    if (sqlite3_prepare_v2(db, query.c_str(), -1, &stmt, nullptr) != SQLITE_OK) {
        return results;
    }

    while (sqlite3_step(stmt) == SQLITE_ROW) {
        std::vector<std::string> row;
        for (int i = 0; i < sqlite3_column_count(stmt); i++) {
            const unsigned char* text = sqlite3_column_text(stmt, i);
            if (text) {
                row.push_back(std::string(reinterpret_cast<const char*>(text)));
            } else {
                row.push_back("");
            }
        }
        results.push_back(row);
    }

    sqlite3_finalize(stmt);
    return results;
}


template<typename T, typename F>
/**
 * Compares two items based on their mapped values using a given key mapping function.
 * 
 * @param key_map A function object to extract the value to compare from each item.
 * @param item1 The first item to be compared.
 * @param item2 The second item to be compared.
 * @return -1 if the mapped value of item1 is less than that of item2, 
 *          1 if the mapped value of item1 is greater than that of item2, 
 *          0 if both mapped values are equal.
 */
int compare(F key_map, const T& item1, const T& item2) {
    auto val1 = key_map(item1);
    auto val2 = key_map(item2);

    if (val1 < val2) return -1;
    if (val1 > val2) return 1;
    return 0;
}
